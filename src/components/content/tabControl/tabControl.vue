<template>
  <div class="tab-control">
    <div class="tab-control-item" :class="{active: currentIndex === index}"
         v-for="(item, index) in titles" @click="itemClick(index)">
      <span>{{item}}</span>
    </div>
  </div>
</template>

<script>
  export default {
    name: "tabControl",
    data(){
      return{
        currentIndex: 0
      }
    },
    methods: {
      itemClick(index){
        this.currentIndex = index
        this.$emit('tabClick', index)
      }
    },
    props: {
      titles: {
        type: Array,
        default: []
      }
    }
  }
</script>

<style scoped>
  .tab-control{
    display: flex;
    text-align: center;
